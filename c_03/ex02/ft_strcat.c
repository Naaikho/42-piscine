char	*ft_strcat(char *dest, char *src)
{
	int i;
	int l;

	i = 0;
	l = 0;
	while (dest[i])
		i++;
	while (src[l])
	{
		dest[i] = src[l];
		l++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
