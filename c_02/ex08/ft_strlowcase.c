char	*ft_strlowcase(char *str)
{
	unsigned int i;

	i = 0;
	while (1)
	{
		if (str[i] == '\0')
		{
			break ;
		}
		else
		{
			if ((str[i]) >= 65 && (str[i]) <= 90)
				str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
