int		ft_str_is_alpha(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '\0')
		{
			break ;
		}
		if ((int)(str[i]) >= 65 && (int)(str[i]) <= 90)
		{
			;
		}
		else if ((int)(str[i]) >= 97 && (int)(str[i]) <= 122)
		{
			;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (1);
}
