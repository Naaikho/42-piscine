void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int l;
	int cp[size];

	i = 0;
	l = size - 1;
	while (i < size)
	{
		cp[l] = tab[i];
		l--;
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = cp[i];
		i++;
	}
}
